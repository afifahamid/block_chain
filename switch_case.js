var grade = "wearing mask";

switch(grade) {

case "wearing mask": {
    console.log("Please go inside");
    break;
    }

case "mask is down": {
    console.log("Put on your mask please");
    break;
    }

case "mask is in hand": {
    console.log("Please wear your mask");
    break;
    }

case "no mask": {
    console.log("You cannot go inside");
    break;
    }

default: {
    console.log("Please buy mask");
    break;
    }

   }